    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
       DefaultTableModel rtm=(DefaultTableModel)result_table.getModel();
       rtm.setRowCount(0);
       rtm.setColumnCount(0);
       
       IScheduler is = new IScheduler();
       Vector v1=new Vector();
       for(int a=0;a<is.data_base.size();a++){
       v1.add(is.data_base.get(a).getId());
       }
       rtm.addColumn("TIME (S)", v1);
       
      
       
       
       for(int i=0;i<is.tk.getMax_time();i++){
           Vector v = new Vector();
           if(is.tk.result[i].equals("non")){
               v.add("");
           }else{
           for(int x=0;x<is.data_base.size();x++){
                if(is.tk.result[i].equals(is.data_base.get(x).getId())){
                    for(int y= 0;y<x;y++){
                     v.add("");
                    }
                    v.add("R");
                    
                }
                
           }
           }
           rtm.addColumn(i+1, v);
       }
    }//GEN-LAST:event_jButton1ActionPerformed

